// Metode untuk operasi matematika

// 1. Math.abs(x): Mengembalikan nilai absolut dari x
Math.abs(-5); // Output: 5

// 2. Math.acos(x): Mengembalikan arccosinus dari x, dalam radian
Math.acos(0.5); // Output: 1.0471975511965976

// 3. Math.acosh(x): Mengembalikan arccosinus hiperbolik dari x
Math.acosh(10); // Output: 2.993222846126381

// 4. Math.asin(x): Mengembalikan arcsinus dari x, dalam radian
Math.asin(0.5); // Output: 0.5235987755982989

// 5. Math.asinh(x): Mengembalikan arcsinus hiperbolik dari x
Math.asinh(2); // Output: 1.4436354751788103

// 6. Math.atan(x): Mengembalikan arktangen dari x, sebagai nilai numerik antara -PI/2 dan PI/2 radian
Math.atan(1); // Output: 0.7853981633974483

// 7. Math.atan2(y, x): Mengembalikan arktangen dari kuotien dari argumennya
Math.atan2(1, 1); // Output: 0.7853981633974483

// 8. Math.atanh(x): Mengembalikan arktangen hiperbolik dari x
Math.atanh(0.5); // Output: 0.5493061443340549

// 9. Math.cbrt(x): Mengembalikan akar kubik dari x
Math.cbrt(8); // Output: 2

// 10. Math.ceil(x): Mengembalikan x, dibulatkan ke atas ke bilangan bulat terdekat
Math.ceil(5.3); // Output: 6

// 11. Math.cos(x): Mengembalikan kosinus dari x (x dalam radian)
Math.cos(Math.PI); // Output: -1

// 12. Math.cosh(x): Mengembalikan kosinus hiperbolik dari x
Math.cosh(0); // Output: 1

// 13. Math.exp(x): Mengembalikan nilai dari Ex
Math.exp(1); // Output: 2.718281828459045

// 14. Math.floor(x): Mengembalikan x, dibulatkan ke bawah ke bilangan bulat terdekat
Math.floor(5.8); // Output: 5

// 15. Math.log(x): Mengembalikan logaritma natural (basis E) dari x
Math.log(Math.E); // Output: 1

// 16. Math.max(x, y, z, ..., n): Mengembalikan angka dengan nilai tertinggi
Math.max(10, 20, 30); // Output: 30

// 17. Math.min(x, y, z, ..., n): Mengembalikan angka dengan nilai terendah
Math.min(10, 20, 30); // Output: 10

// 18. Math.pow(x, y): Mengembalikan nilai x pangkat y
Math.pow(2, 3); // Output: 8

// 19. Math.random(): Mengembalikan angka acak antara 0 dan 1
Math.random(); // Output: Angka acak antara 0 dan 1

// 20. Math.round(x): Membulatkan x ke bilangan bulat terdekat
Math.round(5.5); // Output: 6

// 21. Math.sign(x): Mengembalikan tanda dari x (-1 jika x negatif, 0 jika x nol, 1 jika x positif)
Math.sign(-5); // Output: -1

// 22. Math.sin(x): Mengembalikan sinus dari x (x dalam radian)
Math.sin(Math.PI / 2); // Output: 1

// 23. Math.sinh(x): Mengembalikan sinus hiperbolik dari x
Math.sinh(0); // Output: 0

// 24. Math.sqrt(x): Mengembalikan akar kuadrat dari x
Math.sqrt(16); // Output: 4

// 25. Math.tan(x): Mengembalikan tangen dari sudut x
Math.tan(Math.PI / 4); // Output: 1

// 26. Math.tanh(x): Mengembalikan tangen hiperbolik dari x
Math.tanh(0); // Output: 0

// 27. Math.trunc(x): Mengembalikan bagian bilangan bulat dari x
Math.trunc(5.8); // Output: 5